package com.ConditionalStatements;

public class ifElse {

	public static void main(String[] args) {
		
		int a=10;
		
		int b=20;
		
		if((a>=b))
		{
			//body will get execute only on true statement
			System.out.println("A is greater than B");
		}else
		{
			System.out.println("A is not greater than B");
		}

	}

}
