package com.writingsampleprogram;

public class UnderstandingClass {

	public static void main(String[] args) {
		
//sending values is called a arguments
		add(25,89);
		
		add(15,15);
		
		
		add(15,20);
		
	}

	//accepting values is called as parameters
	public static void add(int a,int b)
	{
		
		int result;
		
		// assign the values to the above created variables
		
		
		result=a+b;
		
		System.out.println(result);
	}
	
}
